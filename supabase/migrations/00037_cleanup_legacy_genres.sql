-- ============================================
-- 00037: 旧ジャンルデータを新 13 項目以外から除去
-- ============================================
-- AILIER リブランド前の古いジャンル (広告・SNS動画 / ショート / 解説ビデオ ...)
-- が creator_profiles.genres に残っているケースがあるため、新 13 項目に
-- マッチしないエントリを配列から除外する。
--
-- 新ジャンル (constants.ts と同期):
--   SNS広告動画, ブランドムービー, 採用動画, プロダクト紹介動画,
--   サービス解説動画(Explainer), LP用ヒーロー動画, ミュージックビデオ,
--   新商品ローンチ動画, 展示会・イベント映像, AIアバター・キャラクター動画,
--   多言語ローカライズ動画, ショートフィルム・アート映像, その他カスタム動画

UPDATE creator_profiles
SET genres = (
  SELECT COALESCE(array_agg(g), ARRAY[]::TEXT[])
  FROM unnest(genres) g
  WHERE g IN (
    'SNS広告動画',
    'ブランドムービー',
    '採用動画',
    'プロダクト紹介動画',
    'サービス解説動画(Explainer)',
    'LP用ヒーロー動画',
    'ミュージックビデオ',
    '新商品ローンチ動画',
    '展示会・イベント映像',
    'AIアバター・キャラクター動画',
    '多言語ローカライズ動画',
    'ショートフィルム・アート映像',
    'その他カスタム動画'
  )
);
